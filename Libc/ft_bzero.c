#include <stddef.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	len;

	len = 0;
	while (len < n)
	{
		((char*)s)[len] = 0;
		len ++;
	}
}

int main(void)
{
	ft_bzero("hola", 3);
}
